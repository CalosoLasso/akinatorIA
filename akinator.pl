adivinar :-
    limpiar_respuestas,
    findall(X, personaje(X, _), Lista),
    buscar(Lista).

buscar([Personaje]) :-
    write('¡Lo tengo! El personaje es: '), write(Personaje), nl, !.

buscar([]) :-
    write('No pude identificar al personaje con tus respuestas. Intenta de nuevo.'), nl, !.

buscar(Lista) :-
    obtener_caracteristica_util(Lista, C),
    !, % Este corte evita que Prolog intente otras reglas si el usuario se equivoca
    preguntar(C),
    filtrar_personajes(Lista, C, NuevaLista),
    buscar(NuevaLista).

buscar(Lista) :-
    write('No puedo reducirlo a uno solo. Podrían ser: '), write(Lista), nl.

obtener_caracteristica_util(Lista, C) :-
    member(Pe, Lista),
    personaje(Pe, Rasgos),
    member(C, Rasgos),
    \+ respuesta(C, _),
    sirve_para_dividir(Lista, C),
    !.

sirve_para_dividir(Lista, C) :-
    aparece_en_algunos(Lista, C),
    no_aparece_en_algunos(Lista, C).

aparece_en_algunos(Lista, C) :-
    member(Pe, Lista),
    personaje(Pe, L),
    member(C, L),
    !.

no_aparece_en_algunos(Lista, C) :-
    member(Pe, Lista),
    personaje(Pe, L),
    \+ member(C, L),
    !.


preguntar(C) :-
    write('¿El personaje tiene la caracteristica "'), write(C), write('"? (s./n.): '),
    read(Respuesta),
    validar_respuesta(C, Respuesta).

validar_respuesta(C, s) :-
    assert(respuesta(C, s)), !.
validar_respuesta(C, n) :-
    assert(respuesta(C, n)), !.
validar_respuesta(C, _) :-
    
    write('Error: Por favor responde unicamente con "s." o "n." (no olvides el punto al final).'), nl,
    preguntar(C).

filtrar_personajes([], _, []).

filtrar_personajes([Pe|R], C, [Pe|R2]) :-
    personaje(Pe, Lista),
    member(C, Lista),
    respuesta(C, s),
    !,
    filtrar_personajes(R, C, R2).

filtrar_personajes([Pe|R], C, [Pe|R2]) :-
    personaje(Pe, Lista),
    \+ member(C, Lista),
    respuesta(C, n),
    !,
    filtrar_personajes(R, C, R2).

filtrar_personajes([_|R], C, R2) :-
    filtrar_personajes(R, C, R2).

limpiar_respuestas :-
    retractall(respuesta(_, _)).
